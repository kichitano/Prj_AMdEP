package com.example.prj_amdep.Model;

public class HelpRequestModel {

    String userDNI, stationID, requestLatitude, requestLongitude, requestDate, requestTime, requestInformation, requestTaken, requestStatus;

    public String getUserDNI() {
        return userDNI;
    }

    public void setUserDNI(String userDNI) {
        this.userDNI = userDNI;
    }

    public String getStationID() {
        return stationID;
    }

    public void setStationID(String stationID) {
        this.stationID = stationID;
    }

    public String getRequestLatitude() {
        return requestLatitude;
    }

    public void setRequestLatitude(String requestLatitude) {
        this.requestLatitude = requestLatitude;
    }

    public String getRequestLongitude() {
        return requestLongitude;
    }

    public void setRequestLongitude(String requestLongitude) {
        this.requestLongitude = requestLongitude;
    }

    public String getRequestDate() {
        return requestDate;
    }

    public void setRequestDate(String requestDate) {
        this.requestDate = requestDate;
    }

    public String getRequestTime() {
        return requestTime;
    }

    public void setRequestTime(String requestTime) {
        this.requestTime = requestTime;
    }

    public String getRequestInformation() {
        return requestInformation;
    }

    public void setRequestInformation(String requestInformation) {
        this.requestInformation = requestInformation;
    }

    public String getRequestTaken() {
        return requestTaken;
    }

    public void setRequestTaken(String requestTaken) {
        this.requestTaken = requestTaken;
    }

    public String getRequestStatus() {
        return requestStatus;
    }

    public void setRequestStatus(String requestStatus) {
        this.requestStatus = requestStatus;
    }
}
